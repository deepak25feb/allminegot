package com.example.firebasetest2;

public class Model {
    int image1;
    int image2;

    public Model(int image1, int image2) {
        this.image1 = image1;
        this.image2 = image2;
    }

    public int getImage1() {
        return image1;
    }

    public void setImage1(int image1) {
        this.image1 = image1;
    }

    public int getImage2() {
        return image2;
    }

    public void setImage2(int image2) {
        this.image2 = image2;
    }
}
